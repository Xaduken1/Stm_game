void FB_Clear(uint8_t color)
{
    memset(g_framebuffer, color ? 0xFF : 0x00, FB_SIZE);
}

void FB_DrawPixel(int x, int y, uint8_t color)
{
    if (x < 0 || x >= FB_WIDTH || y < 0 || y >= FB_HEIGHT)
        return;

    uint16_t index = x + (y / 8) * FB_WIDTH;
    uint8_t bit = 1 << (y % 8);

    if (color)
        g_framebuffer[index] |= bit;
    else
        g_framebuffer[index] &= ~bit;
}

void FB_DrawRect(int x, int y, int w, int h, uint8_t color)
{
    for (int i = x; i < x + w; i++)
    {
        FB_DrawPixel(i, y, color);
        FB_DrawPixel(i, y + h - 1, color);
    }

    for (int j = y; j < y + h; j++)
    {
        FB_DrawPixel(x, j, color);
        FB_DrawPixel(x + w - 1, j, color);
    }
}

void FB_FillRect(int x, int y, int w, int h, uint8_t color)
{
    for (int j = y; j < y + h; j++)
    {
        for (int i = x; i < x + w; i++)
        {
            FB_DrawPixel(i, j, color);
        }
    }
}

void FB_DrawChar6x8(int x, int y, char c, uint8_t color)
{
    if (c < 32 || c > 90)
        c = ' ';

    const uint8_t *glyph = font6x8[c - 32];

    for (int col = 0; col < 6; col++)
    {
        uint8_t bits = glyph[col];
        for (int row = 0; row < 8; row++)
        {
            if (bits & (1 << row))
                FB_DrawPixel(x + col, y + row, color);
        }
    }
}

void FB_DrawString6x8(int x, int y, const char *str, uint8_t color)
{
    while (*str)
    {
        FB_DrawChar6x8(x, y, *str, color);
        x += 6;
        str++;
    }
}
